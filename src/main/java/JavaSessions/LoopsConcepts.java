package JavaSessions;

public class LoopsConcepts {

	public static void main(String[] args) {
		// 1.While Loop
		
		int i=0;
		while(i<=10)
		{
			System.out.println(i);
			i++;
		}
		
		System.out.println("********************");
		//2.for loop
		for (int i1=0;i1<=10;i1++)
		{
			System.out.println(i1);
		}
		
	}

}
